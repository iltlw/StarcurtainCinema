package com.buka.pojo;



public class CinemaHome {
    private int cinemaHomeId;
    private String cinemaHomeName;
    private String cinemaHomeSeat;
    private int cinemaHomeCinemaId;
    private int cinemaHomeState;

    public int getCinemaHomeId() {
        return cinemaHomeId;
    }

    public void setCinemaHomeId(int cinemaHomeId) {
        this.cinemaHomeId = cinemaHomeId;
    }

    public String getCinemaHomeName() {
        return cinemaHomeName;
    }

    public void setCinemaHomeName(String cinemaHomeName) {
        this.cinemaHomeName = cinemaHomeName;
    }

    public String getCinemaHomeSeat() {
        return cinemaHomeSeat;
    }

    public void setCinemaHomeSeat(String cinemaHomeSeat) {
        this.cinemaHomeSeat = cinemaHomeSeat;
    }

    public int getCinemaHomeCinemaId() {
        return cinemaHomeCinemaId;
    }

    public void setCinemaHomeCinemaId(int cinemaHomeCinemaId) {
        this.cinemaHomeCinemaId = cinemaHomeCinemaId;
    }

    public int getCinemaHomeState() {
        return cinemaHomeState;
    }

    public void setCinemaHomeState(int cinemaHomeState) {
        this.cinemaHomeState = cinemaHomeState;
    }
}
